function abrirBotao() {

  let fontes = document.querySelector(".fontesAzuis");

  if (fontes.style.height == fontes.scrollHeight + "px") {
    fontes.style.height = "0px";

  } else {
    fontes.style.height = fontes.scrollHeight + "px";

  }


}

